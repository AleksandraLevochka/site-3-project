// serach by street 

// Filter courts by street name
function filterCourtsByStreetName(streetName) {
    return brooklynCourts.filter(court => court.Location.toLowerCase().includes(streetName.toLowerCase()));
}

// Function to update the courts list based on the search input
function updateCourtsList() {
    var searchInput = document.getElementById('searchInput').value.trim();
    var filteredCourts = filterCourtsByStreetName(searchInput);
    displayCourts(filteredCourts);
}

// Display filtered courts
function displayCourts(courts) {
    var courtsList = document.getElementById('courtsList');
    courtsList.innerHTML = ''; // Clear previous content

    courts.forEach(court => {
        var courtDiv = document.createElement('div');
        courtDiv.classList.add('court-container');

        var nameSpan = document.createElement('span');
        nameSpan.classList.add('court-name');
        nameSpan.textContent = court.Name;

        var locationSpan = document.createElement('span');
        locationSpan.classList.add('court-location');
        locationSpan.textContent = court.Location;

        courtDiv.appendChild(nameSpan);
        courtDiv.appendChild(locationSpan);

        courtsList.appendChild(courtDiv);
    });
}

